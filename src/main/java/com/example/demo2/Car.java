package com.example.demo2;

public class Car {

    private int seats;
    private int doors;
    private int wheels;

    public void setWheels(int newWheels) {
        wheels = newWheels;
    }

    public int getWheels() {
        return wheels;
    }

    public int getSeats() {
        return seats;
    }

    public void setSeats(int newSeats) {
        seats = newSeats;
    }

    public int getDoors() {
        return doors;
    }

    public void setDoors(int newDoors) {
        doors = newDoors;
    }
}

